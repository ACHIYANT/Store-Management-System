const { Custodian, sequelize, Sequelize } = require("../models");
const { Op } = require("sequelize");
const { normalizeLimit } = require("../utils/cursor-pagination");
const {
  isKnownDivisionValue,
  normalizeDivisionValue,
} = require("../utils/division-utils");
const {
  LOCATION_OPTIONS,
  normalizeLocationValue,
} = require("../utils/location-options");

const normalizeType = (value) =>
  value == null ? null : String(value).trim().toUpperCase();
const normalizeDisplayName = (value) => {
  if (value == null) return null;
  const text = String(value).trim().replace(/\s+/g, " ");
  return text || null;
};
const normalizeLocation = (value) => {
  if (value == null) return null;
  const text = String(value).trim().replace(/\s+/g, " ");
  return text || null;
};
const normalizeScopeToken = (value) => {
  if (value == null) return null;
  const text = String(value).trim().replace(/\s+/g, " ").toUpperCase();
  return text || null;
};
const buildScopeKey = ({ type, displayName, location }) => {
  if (!["DIVISION", "VEHICLE"].includes(type)) return null;
  const normalizedName = normalizeScopeToken(displayName);
  const normalizedLocation = normalizeScopeToken(location);
  if (!normalizedName || !normalizedLocation) return null;
  return `${type}|${normalizedLocation}|${normalizedName}`;
};
const toLocationCode = (location) => {
  const text = normalizeLocation(location);
  if (!text) return null;
  const code = text.toUpperCase().replace(/[^A-Z0-9]/g, "");
  if (!code) return null;
  return code.slice(0, 3);
};

const AUTO_ID_PREFIX_BY_TYPE = {
  DIVISION: "DIV",
  VEHICLE: "VEH",
};

const MIN_SEQUENCE_WIDTH = 3;
const ALLOWED_LOCATION_LIST = LOCATION_OPTIONS.map((option) => option.value).join(
  ", ",
);

const buildNextCustodianSequence = async ({ type, transaction }) => {
  const prefix = AUTO_ID_PREFIX_BY_TYPE[type];
  if (!prefix) return null;

  const likePattern = `${prefix}-%`;
  const rows = await sequelize.query(
    `
      SELECT id
      FROM Custodians
      WHERE custodian_type = :type
        AND id LIKE :likePattern
      ORDER BY CAST(SUBSTRING_INDEX(id, '-', -1) AS UNSIGNED) DESC
      LIMIT 1
      FOR UPDATE
    `,
    {
      replacements: { type, likePattern },
      type: Sequelize.QueryTypes.SELECT,
      transaction,
    },
  );

  const lastId = rows?.[0]?.id ? String(rows[0].id) : "";
  const match = lastId.match(/-(\d+)$/);
  const lastSequence = Number.parseInt(match?.[1] || "0", 10);
  const nextSequence = Number.isFinite(lastSequence) ? lastSequence + 1 : 1;

  return nextSequence;
};

const buildNextCustodianId = async ({
  type,
  locationCode,
  transaction,
}) => {
  const prefix = AUTO_ID_PREFIX_BY_TYPE[type];
  if (!prefix) return null;
  const sequence = await buildNextCustodianSequence({ type, transaction });
  const padded = String(sequence).padStart(MIN_SEQUENCE_WIDTH, "0");
  return locationCode ? `${prefix}-${locationCode}-${padded}` : `${prefix}-${padded}`;
};

class CustodianRepository {
  async createCustodian(payload) {
    const providedId = payload?.id != null ? String(payload.id).trim() : "";
    const rawDisplayName = normalizeDisplayName(
      payload?.display_name ?? payload?.displayName,
    );
    let displayName = rawDisplayName;
    const type = normalizeType(payload?.custodian_type ?? payload?.custodianType);
    const rawLocation = normalizeLocation(payload?.location);
    const location = normalizeLocationValue(rawLocation);
    const locationCode = toLocationCode(location);

    if (!displayName) throw new Error("display_name is required");
    if (!type) throw new Error("custodian_type is required");
    if (rawLocation && !location) {
      throw new Error(`location must be one of: ${ALLOWED_LOCATION_LIST}.`);
    }
    if (AUTO_ID_PREFIX_BY_TYPE[type] && !locationCode) {
      throw new Error(
        `location is required and must be one of: ${ALLOWED_LOCATION_LIST}.`,
      );
    }
    if (!providedId && !AUTO_ID_PREFIX_BY_TYPE[type]) {
      throw new Error("custodian id is required");
    }

    if (type === "DIVISION") {
      const normalizedDivision = normalizeDivisionValue(displayName);
      if (!normalizedDivision || !isKnownDivisionValue(normalizedDivision)) {
        throw new Error("display_name must be one of the configured divisions.");
      }
      displayName = normalizedDivision;
    }

    const scopeKey = buildScopeKey({ type, displayName, location });

    return sequelize.transaction(async (transaction) => {
      const autoGeneratedId = providedId
        ? null
        : await buildNextCustodianId({
            type,
            locationCode,
            transaction,
          });
      const id = providedId || autoGeneratedId;
      if (scopeKey) {
        const existing = await Custodian.findOne({
          where: { scope_key: scopeKey },
          attributes: ["id", "display_name", "location"],
          transaction,
          lock: transaction.LOCK.UPDATE,
        });
        if (existing) {
          throw new Error(
            `${type} custodian '${displayName}' already exists for location '${location}'`,
          );
        }
      }

      return Custodian.create(
        {
          id,
          custodian_type: type,
          display_name: displayName,
          location,
          scope_key: scopeKey,
          employee_id: null,
          is_active: true,
        },
        {
          transaction,
        },
      );
    });
  }

  async getById(id) {
    return Custodian.findByPk(String(id));
  }

  async list({
    search = "",
    custodian_type = "",
    location = "",
    is_active = true,
    page = null,
    limit = null,
  } = {}) {
    const where = {};
    const type = normalizeType(custodian_type);
    if (type) where.custodian_type = type;
    const cleanLocation = normalizeLocation(location);
    if (cleanLocation) {
      where.location = {
        [Op.like]: `%${cleanLocation}%`,
      };
    }
    if (typeof is_active === "boolean") {
      where.is_active = is_active;
    }

    const searchTerm = String(search || "").trim();
    if (searchTerm) {
      const like = `%${searchTerm}%`;
      where[Op.or] = [
        { id: { [Op.like]: like } },
        { display_name: { [Op.like]: like } },
        { location: { [Op.like]: like } },
      ];
    }

    const safeLimit =
      limit != null && String(limit).trim() !== ""
        ? normalizeLimit(limit, 50, 500)
        : null;
    const safePage =
      page != null && Number.isFinite(Number(page)) && Number(page) > 0
        ? Number(page)
        : null;

    const order = [
      ["display_name", "ASC"],
      ["id", "ASC"],
    ];

    if (!safeLimit || !safePage) {
      return Custodian.findAll({ where, order });
    }

    const offset = (safePage - 1) * safeLimit;
    return Custodian.findAndCountAll({ where, order, limit: safeLimit, offset });
  }
}

module.exports = CustodianRepository;
