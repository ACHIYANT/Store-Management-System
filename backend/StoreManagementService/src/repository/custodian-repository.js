const { Custodian, sequelize, Sequelize } = require("../models");
const { Op } = require("sequelize");
const { normalizeLimit } = require("../utils/cursor-pagination");

const normalizeType = (value) =>
  value == null ? null : String(value).trim().toUpperCase();

const AUTO_ID_PREFIX_BY_TYPE = {
  DIVISION: "DIV",
  VEHICLE: "VEH",
};

const MIN_SEQUENCE_WIDTH = 2;

const buildNextCustodianId = async ({ type, transaction }) => {
  const prefix = AUTO_ID_PREFIX_BY_TYPE[type];
  if (!prefix) return null;

  const likePattern = `${prefix}-%`;
  const rows = await sequelize.query(
    `
      SELECT id
      FROM Custodians
      WHERE custodian_type = :type
        AND id LIKE :likePattern
      ORDER BY CAST(SUBSTRING_INDEX(id, '-', -1) AS UNSIGNED) DESC
      LIMIT 1
      FOR UPDATE
    `,
    {
      replacements: { type, likePattern },
      type: Sequelize.QueryTypes.SELECT,
      transaction,
    },
  );

  const lastId = rows?.[0]?.id ? String(rows[0].id) : "";
  const lastSequence = Number.parseInt(lastId.split("-").pop() || "0", 10);
  const nextSequence = Number.isFinite(lastSequence) ? lastSequence + 1 : 1;

  return `${prefix}-${String(nextSequence).padStart(MIN_SEQUENCE_WIDTH, "0")}`;
};

class CustodianRepository {
  async createCustodian(payload) {
    const providedId = payload?.id != null ? String(payload.id).trim() : "";
    const displayName =
      payload?.display_name != null
        ? String(payload.display_name).trim()
        : payload?.displayName != null
          ? String(payload.displayName).trim()
          : "";
    const type = normalizeType(payload?.custodian_type ?? payload?.custodianType);

    if (!displayName) throw new Error("display_name is required");
    if (!type) throw new Error("custodian_type is required");
    if (!providedId && !AUTO_ID_PREFIX_BY_TYPE[type]) {
      throw new Error("custodian id is required");
    }

    return sequelize.transaction(async (transaction) => {
      const autoGeneratedId = providedId
        ? null
        : await buildNextCustodianId({
            type,
            transaction,
          });
      const id = providedId || autoGeneratedId;

      return Custodian.create({
        id,
        custodian_type: type,
        display_name: displayName,
        employee_id: null,
        is_active: true,
      }, {
        transaction,
      });
    });
  }

  async getById(id) {
    return Custodian.findByPk(String(id));
  }

  async list({
    search = "",
    custodian_type = "",
    page = null,
    limit = null,
  } = {}) {
    const where = {};
    const type = normalizeType(custodian_type);
    if (type) where.custodian_type = type;

    const searchTerm = String(search || "").trim();
    if (searchTerm) {
      const like = `%${searchTerm}%`;
      where[Op.or] = [
        { id: { [Op.like]: like } },
        { display_name: { [Op.like]: like } },
      ];
    }

    const safeLimit =
      limit != null && String(limit).trim() !== ""
        ? normalizeLimit(limit, 50, 500)
        : null;
    const safePage =
      page != null && Number.isFinite(Number(page)) && Number(page) > 0
        ? Number(page)
        : null;

    const order = [
      ["display_name", "ASC"],
      ["id", "ASC"],
    ];

    if (!safeLimit || !safePage) {
      return Custodian.findAll({ where, order });
    }

    const offset = (safePage - 1) * safeLimit;
    return Custodian.findAndCountAll({ where, order, limit: safeLimit, offset });
  }
}

module.exports = CustodianRepository;
